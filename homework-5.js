// Далее создаем функцию, которая принимает 1 параметр - текущий бюджет, 
// внутри функции происходит проверка: если бюджет превышает цену товара - выводим
// лог "(ваше название товара) приобретён. Спасибо за покупку!", 
// если нет - обсчитываем разницу и выводим лог "Вам не хватает X$, 
// пополните баланс".  То-есть с помощью функции мы пытаемся приобрести товар.

const product = 'Сырок'
const price = 67

function calculateBudget(x) {
  const difference = price - x

  if (x >= price) {
    console.log(product, 'приобретён. Спасибо за покупку!')
  }

  else {
    console.log('Вам не хватает', difference + '$,', 'пополните баланс')
  }
}

